package Aulas;

public class TiposNumericos {

	public static void main(String[] args) {
		@SuppressWarnings("unused")
		Byte numeroByte = 1; 			// -128 até 127
		
		@SuppressWarnings("unused")
		Short numeroShort = 1; 			// -32768 até 32767
		
		@SuppressWarnings("unused")
		Integer numeroInteger = 1; 		// -2147483648 até 2147483647
		
		@SuppressWarnings("unused")
		Long numeroLong = 1L;			// -9223372036854775808 até 9223372036854775807
		
		
		@SuppressWarnings("unused")
		Float numeroFloat = 1.0f;
		
		@SuppressWarnings("unused")
		Double numeroDouble = 1.0;
	}
}
